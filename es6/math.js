//Math.trunc()
Math.trunc(4.1) // 4
Math.trunc(4.9) // 4
Math.trunc(-4.1) // -4
Math.trunc(-4.9) // -4
Math.trunc(-0.1234) // -0

//Math.sign()
Math.sign(-5) // -1
Math.sign(5) // +1
Math.sign(0) // +0
Math.sign(-0) // -0
Math.sign(NaN) // NaN

//Math.cbrt() Math.cbrt方法用于计算一个数的立方根。
Math.cbrt('8') // 2
Math.cbrt('hello') // NaN


//指数运算符 **

2 ** 2 // 4 2的2次方
2 ** 3 // 8 2的3次方